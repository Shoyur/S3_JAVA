package modeles;

public class Livre {
	private int no;
	private String titre;
	private int auteur;
	private int annee;
	private int pages;
	private String categ;
    
    public int getNo() {
        return no;
    }
    public void setNo(int no) {
        this.no = no;
    }
    public String getTitre() {
        return titre;
    }
    public void setTitre(String titre) {
        this.titre = titre;
    }
    public int getAuteur() {
        return auteur;
    }
    public void setAuteur(int auteur) {
        this.auteur = auteur;
    }
    public int getAnnee() {
        return annee;
    }
    public void setAnnee(int annee) {
        this.annee = annee;
    }
    public int getPages() {
        return pages;
    }
    public void setPages(int pages) {
        this.pages = pages;
    }
    public String getCateg() {
        return categ;
    }
    public void setCateg(String categ) {
        this.categ = categ;
    }


}
